            const container = document.getElementById("pecContainer");

            fetch('centres.json')
                .then(response => response.json())
                .then(data => {
                    // Filter out empty items and clean up the data
                    // const pecData = data.filter(item => item.centre && item.centre.trim() !== "").map(item => {
                    //     if (item.images) {
                    //         // Remove base64/SVG placeholders and empty strings
                    //         item.images = item.images.filter(img => img && !img.startsWith('data:image/svg+xml') && img.trim() !== '');
                    //     }
                    //     return item;
                    // });
                    // Store globally so the nextImg/prevImg functions can access the cleaned image arrays
                    window.pecData = data;
                    container.innerHTML = '';
                    console.log(pecData)
                    pecData.forEach((item, index) => {
                        const firstImg = item.images && item.images.length > 0 ? item.images[0] : '';
                        let arrowsphp = '';

                        if (item.images && item.images.length > 1) {
                            arrowsphp = `
                            <button class="btn btn-sm btn-dark position-absolute top-50 start-0 translate-middle-y ms-2 rounded-circle border-0" style="opacity: 0.7; z-index: 10; width: 32px; height: 32px; align-items: centre; justify-content: centre;" onclick="prevImg(${index})"><i class="fas fa-chevron-left text-white small"></i></button>
                            <button class="btn btn-sm btn-dark position-absolute top-50 end-0 translate-middle-y me-2 rounded-circle border-0" style="opacity: 0.7; z-index: 10; width: 32px; height: 32px;  align-items: centre; justify-content: centre;" onclick="nextImg(${index})"><i class="fas fa-chevron-right text-white small"></i></button>
                        `;
                        }
                        // Build video link button if present
                        let videoBtnphp = '';
                        if (item.video_link) {
                            // Pass URL into the global click handler
                            videoBtnphp = `
                            <button onclick="openVideoModal('${item.video_link}')" class="btn btn-danger btn-sm rounded-circle align-items-centre justify-content-centre shadow-sm" style="width: 38px; height: 38px;" title="Watch Video">
                                <i class="fas fa-play" style="margin-left: 2px;"></i>
                            </button>
                        `;
                        }

                        const codeDisplay = item.centre_code ? `<span class="badge bg-primary-subtle text-primary px-3 py-2 border rounded-pill">${item.centre_code}</span>` : '';
                        const defaultImage = '<div class="d-flex align-items-centre justify-content-centre h-100 bg-secondary-subtle"><i class="fas fa-image text-secondary fa-3x"></i></div>';

                        container.innerHTML += `
                        <div class="col-12 col-sm-6 col-md-6 col-xxl-4 d-flex mb-4">
                            <div class="card h-100 shadow-sm border-0 w-100 d-flex flex-column" style="border-radius: 12px; overflow: hidden; background-color: #fff;">
                                <div class="img-container position-relative bg-light text-centre" style="height: 240px; overflow: hidden;">
                                    ${firstImg ? `<img id="img-${index}" src="${firstImg}" alt="centre Image" style="width: 100%; height: 100%; object-fit: cover;">` : defaultImage}
                                    ${arrowsphp}
                                </div>
                                <div class="card-body d-flex flex-column p-4">
                                    <div class="text-danger small mb-2 fw-bold" style="letter-spacing: 0.5px; text-transform: uppercase;">${item.project || 'Project Educate centre'}</div>
                                    <h5 class="school-name fw-bold mb-3 text-dark" style="line-height: 1.4; font-size: 1.15rem;">${item.centre}</h5>
                                    
                                    ${item.location ? `<div class="location small text-secondary mb-3"><i class="fas fa-map-marker-alt me-2 text-danger"></i>${item.location}</div>` : ''}
                                    
                                    <div class="d-flex align-items-centre justify-content-between mt-auto pt-3 border-top w-100">
                                        ${codeDisplay}
                                        ${videoBtnphp}
                                    </div>
                                </div>
                            </div>
                        </div>
                    `;
                    });
                })
                .catch(error => console.error('Error fetching centres.json:', error));

            // Image index tracker
            const imgIndex = {};

            function nextImg(i) {
                if (!window.pecData || !window.pecData[i] || !window.pecData[i].images.length) return;
                imgIndex[i] = (imgIndex[i] ?? 0) + 1;
                if (imgIndex[i] >= window.pecData[i].images.length) imgIndex[i] = 0;
                document.getElementById(`img-${i}`).src = window.pecData[i].images[imgIndex[i]];
            }

            function prevImg(i) {
                if (!window.pecData || !window.pecData[i] || !window.pecData[i].images.length) return;
                imgIndex[i] = (imgIndex[i] ?? 0) - 1;
                if (imgIndex[i] < 0) imgIndex[i] = window.pecData[i].images.length - 1;
                document.getElementById(`img-${i}`).src = window.pecData[i].images[imgIndex[i]];
            }

            // Video Modal Handler
            function openVideoModal(videoUrl) {
                // Append autoplay parameter if not present to start playing immediately in the modal
                if (!videoUrl.includes('autoplay=')) {
                    videoUrl += videoUrl.includes('?') ? '&autoplay=1' : '?autoplay=1';
                }
                document.getElementById('videoIframe').src = videoUrl;
                var myModal = new bootstrap.Modal(document.getElementById('videoModal'));
                myModal.show();
            }

            // Stop video when modal is closed
            document.getElementById('videoModal').addEventListener('hidden.bs.modal', function () {
                document.getElementById('videoIframe').src = "";
            });
